    private void jComboBoxFacultateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxFacultateActionPerformed
        // TODO add your handling code here:
        ArrayList<String> listaNumeSpecializari = new ArrayList<String>();

        String facultateDeCautat = this.jComboBoxFacultate.getSelectedItem().toString();
        int ok=0;
        while(ok==0){
            for (int i = 0; i < this.catalog.facultati.size(); i++) {
                if (facultateDeCautat==this.catalog.facultati.get(i).nume){
                    ok=1;
                    for (int j=0;j<this.catalog.facultati.get(i).specializari.size();j++){
                        listaNumeSpecializari.add(this.catalog.facultati.get(i).specializari.get(j).nume);
                    }
                }
            }
        }

        String[] StringArraySpecializari = listaNumeSpecializari.toArray(new String[0]);
        jComboBoxSpecializare.setModel(new javax.swing.DefaultComboBoxModel<>(StringArraySpecializari));
        
    }//GEN-LAST:event_jComboBoxFacultateActionPerformed

