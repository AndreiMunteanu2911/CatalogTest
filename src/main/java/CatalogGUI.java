    private void jButtonAdaugaStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdaugaStudentActionPerformed
        // TODO add your handling code here:
        Facultate fac = new Facultate();
        Specializare spec = new Specializare();
        String NumeFacultateDeCautat = jComboBoxFacultate.getSelectedItem().toString();
        String NumeSpecializareDeCautat = jComboBoxSpecializare.getSelectedItem().toString();
        for (int i=0;i< this.catalog.facultati.size();i++){
            if (NumeFacultateDeCautat==this.catalog.facultati.get(i).nume){
                fac=this.catalog.facultati.get(i);
                for (int j=0;j<this.catalog.facultati.get(i).specializari.size();j++){
                    if (NumeSpecializareDeCautat==this.catalog.facultati.get(i).specializari.get(j).nume){
                        spec=this.catalog.facultati.get(i).specializari.get(j);
                    }
                }
            }
        }
        Student stud=new Student (Integer.parseInt(jTextFieldVarsta.getText()),jTextFieldNume.getText(),jTextFieldPrenume.getText(),jTextFieldCNP.getText(), jTextFieldJudet.getText(),jTextFieldOras.getText(),jTextFieldStrada.getText(),Integer.parseInt(jTextFieldNr.getText()),Integer.parseInt(jTextFieldCodPostal.getText()),jTextFieldTelefon.getText(),fac,spec);
        this.catalog.adaugaStudent(stud);

        Object[] row = { stud.nrMatricol, stud.nume, stud.prenume , stud.facultate.nume };

        DefaultTableModel model = (DefaultTableModel) jTableStudenti.getModel();

        model.addRow(row);
        
        DefaultComboBoxModel<String> model2 = new DefaultComboBoxModel<>(this.catalog.orase.toArray(new String[0]));
        jComboBoxFiltruOras.setModel(model2);
        
    }//GEN-LAST:event_jButtonAdaugaStudentActionPerformed

